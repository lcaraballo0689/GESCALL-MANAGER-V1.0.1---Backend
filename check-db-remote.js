require('dotenv').config({ path: '/opt/gescall/back/.env' });
const { Client } = require('ssh2');

const sshConfig = {
    host: process.env.VICIDIAL_SSH_HOST || '209.38.233.46',
    port: 22,
    username: process.env.VICIDIAL_SSH_USER || 'root',
    password: process.env.VICIDIAL_SSH_PASSWORD,
    readyTimeout: 20000,
    keepaliveInterval: 5000,
    tryKeyboard: true,
};

const command = `
echo "--- Perl mysql module ---"
perl -e "use DBD::mysql; print 'Module OK\n';"

echo "--- MySQL Localhost Connection ---"
mysql -u cron -p1234 -h localhost -D asterisk -e "SELECT count(*) as VicidialUsers FROM vicidial_users"

echo "--- MySQL 127.0.0.1 Connection ---"
mysql -u cron -p1234 -h 127.0.0.1 -D asterisk -e "SELECT 1 as ConnectionOK"
`;

console.log('Checking Remote DB Connectivity...\n');

const conn = new Client();

conn.on('ready', () => {
    conn.exec(command, (err, stream) => {
        if (err) {
            console.error('Exec error:', err);
            conn.end();
            return;
        }
        let output = '';
        stream.on('close', (code) => {
            console.log(output);
            conn.end();
        }).on('data', (data) => {
            output += data;
        }).stderr.on('data', (data) => {
            output += data;
        });
    });
});

conn.on('error', (err) => {
    console.error('SSH Connection error:', err.message);
    process.exit(1);
});

conn.on('keyboard-interactive', (name, instructions, instructionsLang, prompts, finish) => {
    finish([sshConfig.password]);
});

conn.connect(sshConfig);
